target_sum = 34980616.55

def find_float_combination(nums, target_sum):
    """
    从nums中找出一些数它们的和等于target_sum并返回这些数
    :param nums: 给定的浮点数列表
    :param target_sum: 目标和
    :return: 一些数的列表使它们的和等于目标和如果不存在这样的数则返回None
    """
    n = len(nums)
    dp = [[False]*(int(target_sum*100)+1) for _ in range(n+1)]
    for i in range(n+1):
        dp[i][0] = True
    for i in range(1, n+1):
        for j in range(1, int(target_sum*100)+1):
            if j < int(nums[i-1]*100):
                dp[i][j] = dp[i-1][j]
            else:
                dp[i][j] = dp[i-1][j] or dp[i-1][j-int(nums[i-1]*100)]
    if dp[n][int(target_sum*100)]:
        res = []
        i, j = n, int(target_sum*100)
        while i > 0 and j > 0:
            if dp[i-1][j]:
                i -= 1
            else:
                res.append(nums[i-1])
                j -= int(nums[i-1]*100)
                i -= 1
        res.reverse()
        return res
    else:
        return None


result = find_float_combination(nums, target_sum)
